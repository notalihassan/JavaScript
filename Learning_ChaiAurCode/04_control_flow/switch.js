const month = 3;

switch (month) {
    case 1:
        console.log("MOnth is Jan");
        break;
    case 2:
        console.log("MOnth is Feb");
        break;
    case 3:
        console.log("MOnth is Mar");
        break;
    default:
        console.log("Invalid month");
        break;
}

const Month = "Feb"
